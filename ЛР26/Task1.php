            // Указываем имя файла, где хранится счетчик
            $filename = "counter.dat";

            // Открываем файл для чтения
            $fp = @fopen($filename, "r");

            // Если файл успешно открыт
            if ($fp) {
                // Получаем текущее значение счетчика из файла
                $counter = fgets($fp, 10);
                // Закрываем файл
                fclose($fp);
            } else {
                // Если файл не удалось открыть, устанавливаем счетчик в 0
                $counter = 0;
            }

            // Увеличиваем значение счетчика на 1
            $counter++;

            // Открываем файл для записи
            $fp = @fopen($filename, "w");

            // Если файл успешно открыт
            if ($fp) {
                // Записываем новое значение счетчика в файл
                $counter = fputs($fp, $counter);
                // Закрываем файл
                fclose($fp);
            }

            // Выводим значение счетчика
            echo $counter;
        ?></p>
