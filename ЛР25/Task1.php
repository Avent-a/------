<?php
// 1. Создаем индексированный массив товаров
$tovars = array('Товар1', 'Товар2', 'Товар3', 'Товар4', 'Товар5');

// 2. Добавляем элементы с помощью идентификатора массива
$tovars[] = 'Товар6';
$tovars[] = 'Товар7';

// 3. Определяем количество элементов и выводим в цикле for
$count = count($tovars);
echo "Количество товаров: $count <br>";

for ($i = 0; $i < $count; $i++) {
    echo $tovars[$i] . '<br>';
}

// 4. Сортируем и выводим массив в алфавитном порядке
sort($tovars);
echo "Исходный массив: " . implode(', ', $tovars) . '<br>';
?>
