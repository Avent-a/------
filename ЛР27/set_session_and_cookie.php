<?php
// Начинаем сессию
session_start();

// Устанавливаем переменные a, b, c в рамках сессии
$_SESSION['a'] = 'Значение переменной 1';
$_SESSION['b'] = 'Значение переменной 2';
$_SESSION['c'] = 'Значение переменной 3';

// Устанавливаем переменные a и b в cookie
setcookie('a', 'Значение переменной a в cookie', time() + 3600); // cookie на час
setcookie('b', 'Значение переменной b в cookie', time() + 3600); // cookie на час

echo "Переменные a, b, c установлены в сессию, переменные a и b также сохранены в cookie.";
